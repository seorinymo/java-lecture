
public class OpExample {

	public static void main(String[] agrs) {
		
		// 1. ++
		int a = 10;
		a ++;  //a = a + 1;
		System.out.println("a="+a);
		++ a;  //a = a + 1;
		System.out.println("++ a = " + a ); //12
		
		//System.out.println(a++);
		System.out.println(++a);
		
		int c = 10;
		System.out.println(++c);
		System.out.println(c);
		
		
		// 2. -- d-- => d = d - 1;
		int d = 10;
		System.out.println(d--);
		System.out.println(d);
		
		int e = 10;
		System.out.println(--e);
		System.out.println(e);

		
		
		
		
		
		
		
		
	}
}
